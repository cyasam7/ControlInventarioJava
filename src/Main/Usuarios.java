    public void crearUsuario(String nombre, String usuario, String contraseña, String rol){
        if(nombre.equals("")){
            JOptionPane.showMessageDialog(this, "El campo nombre completo esta vacio");
        }else{
            if(usuario.equals("")){
                JOptionPane.showMessageDialog(this, "El campo usuario esta vacio");
            }else{
                if(contraseña.equals("")){
                    JOptionPane.showMessageDialog(this, "El campo contraseña esta vacio");
                }else{
                    if(nombre.equals("")){
                        JOptionPane.showMessageDialog(this, "El campo Rol esta vacio");
                    }else{
                        try {
                            con.setAutoCommit(false);
                            PreparedStatement ps = con.prepareStatement("INSERT INTO `inventario`.`usuarios` "
                            + "(`nombrecompleto`, `usuario`, `password`, `tipousuario`) "
                            + "VALUES (?,?,?,?)");
                            ps.setString(1, nombre);
                            ps.setString(2, usuario);
                            ps.setString(3, contraseña);
                            ps.setString(4, rol);
                            ps.execute();
                            ps.close();
                            con.commit();
                            JOptionPane.showMessageDialog(this, "Se creo el nuevo usuario correctamente");
                        } catch (SQLException e) {
                            JOptionPane.showMessageDialog(this, "Error al registrar" + e.getMessage());
                        }
                    }
                }
            }
        }
    }
    
